function folderExists(folder) {
  try {
    return fsSync.statSync(folder).isDirectory();
  } catch {
    return false;
  }
}

function windowsAppDataPath() {
  return process.env.APPDATA || path.join(os.homedir(), "AppData", "Roaming");
}

function windowsLocalAppDataPath() {
  return process.env.LOCALAPPDATA || path.join(os.homedir(), "AppData", "Local");
}

function hasExistingConnector(target) {
  return Boolean(findExistingConnectorPath(target));
}

function findExistingConnectorPath(target) {
  if (fileLooksLikeConnector(target.scriptPath)) return target.scriptPath;
  if (!fsSync.existsSync(target.folder)) return null;

  let entries = [];
  try {
    entries = fsSync.readdirSync(target.folder, { withFileTypes: true });
  } catch {
    return null;
  }

  for (const entry of entries) {
    if (!entry.isFile()) continue;
    if (!/\.(lua|luau|txt)$/i.test(entry.name)) continue;
    const filePath = path.join(target.folder, entry.name);
    if (fileLooksLikeConnector(filePath)) return filePath;
  }

  return null;
}

function fileLooksLikeConnector(filePath) {
  if (!fsSync.existsSync(filePath)) return false;
  let text = "";
  try {
    text = fsSync.readFileSync(filePath, "utf8");
  } catch {
    return false;
  }

  return (
    text.includes(AUTOEXEC_MARKER) ||
    (
      text.includes("/script.luau") &&
      text.includes("BridgeURL") &&
      text.includes("localhost:16384")
    )
  );
}

function uniqueAutoexecTargets(targets) {
  const seen = new Set();
  const unique = [];
  for (const target of targets) {
    const key = realTargetKey(target);
    if (seen.has(key)) continue;
    seen.add(key);
    unique.push(target);
  }
  return unique;
}

function realTargetKey(target) {
  try {
    return fsSync.realpathSync.native(target.folder);
  } catch {
    return path.resolve(target.folder).toLowerCase();
  }
}
