import fs from "node:fs/promises";
import fsSync from "node:fs";
import os from "node:os";
import path from "node:path";

export const AUTOEXEC_SCRIPT_NAME = "roblox-executor-mcp.lua";

const AUTOEXEC_MARKER = "-- roblox-executor-mcp autoexec";

export function getAutoexecTargets() {
  const candidates = getAutoexecCandidates();
  return candidates.map((candidate) => {
    const target = {
      ...candidate,
      exists: fsSync.existsSync(candidate.folder),
      scriptPath: path.join(candidate.folder, AUTOEXEC_SCRIPT_NAME),
    };
    return {
      ...target,
      installed: target.exists && hasExistingConnector(target),
      installedPath: target.exists ? findExistingConnectorPath(target) : null,
    };
  });
}

export function getDetectedAutoexecTargets() {
  return uniqueAutoexecTargets(getAutoexecTargets().filter((target) => target.exists));
}

export function getAutoexecStatus() {
  const targets = uniqueAutoexecTargets(getAutoexecTargets());
  return {
    platform: process.platform,
    scriptName: AUTOEXEC_SCRIPT_NAME,
    supported: targets.length > 0,
    targets,
    detectedTargets: uniqueAutoexecTargets(targets.filter((target) => target.exists)),
  };
}

export async function writeLoaderToAutoexec(loaderSnippet, options = {}) {
  const targets = options.targets || getDetectedAutoexecTargets();
  if (!targets.length) {
    return {
      ok: false,
      written: [],
      error: "No supported autoexec folder was found on this machine.",
    };
  }

  const content = formatAutoexecScript(loaderSnippet);
  const written = [];
  for (const target of targets) {
    if (options.dryRun) {
      written.push({
        name: target.name,
        scriptPath: target.scriptPath,
        previousPath: target.installedPath || null,
      });
      continue;
    }
    await fs.mkdir(target.folder, { recursive: true });
    await fs.writeFile(target.scriptPath, content, "utf8");
    written.push({
      name: target.name,
      scriptPath: target.scriptPath,
      previousPath: target.installedPath || null,
    });
  }

  return { ok: true, written, error: null };
}

export function formatAutoexecScript(loaderSnippet) {
  const body = String(loaderSnippet || "").trim();
  return `${AUTOEXEC_MARKER}\n-- Generated by the Roblox Executor MCP installer.\n${body}\n`;
}

